  onLaunch: function () {
    if (!wx.cloud) {
      console.error('请使用 2.2.3 或以上的基础库以使用云能力');
    } else {
      wx.cloud.init({
        // env 参数说明：
        //   env 参数决定接下来小程序发起的云开发调用（wx.cloud.xxx）会默认请求到哪个云环境的资源
        //   此处请填入环境 ID, 环境 ID 可打开云控制台查看
        //   如不填则使用默认环境（第一个创建的环境）
        // env: 'my-env-id',
        traceUser: true,
      });
    }
    // this.globalData = {};
    this.fetchDataFromCOS(); // 从 COS 获取数据
  },
  fetchDataFromCOS() {
    // 从 COS 获取 JSON 数据的示例接口地址
    const cosJsonUrl = 'https://wechatapp-1309811429.cos.ap-shanghai.myqcloud.com/jsonData/course.json';
    wx.request({
      url: cosJsonUrl,
      method: 'GET',
      success: (res) => {
        const cosData = res.data; // 从响应中获取 JSON 数据
        // 处理数据并更新到 storage
        // 遍历cosData中的applyInfo.applyCounts
        cosData.applyInfo.applyCounts.forEach(applyCount => {
          // 在globalData中查找匹配的id
          const matchedData = this.globalData.data.find(data => data.id === applyCount.id);
          // 如果找到了匹配的数据，则更新applyCount字段
          if (matchedData) {
            matchedData.applyCount = applyCount.applyCount;
          }
        });
        // 遍历cosData中的likeInfo.likeCounts
        cosData.likeInfo.likeCounts.forEach(likeCount => {
          // 在globalData中查找匹配的id
          const matchedData = this.globalData.data.find(data => data.id === likeCount.id);
          // 如果找到了匹配的数据，则更新likeCount字段
          if (matchedData) {
            matchedData.likes = likeCount.likeCount;
          }
        });
        // 更新数据到 storage
        wx.setStorageSync('courseList', this.globalData.data);
        wx.setStorageSync('likeCourses', cosData.likeInfo.likeCourses);
      },
      fail: (error) => {
        console.log('获取数据失败', error);
      }
    });
  }
